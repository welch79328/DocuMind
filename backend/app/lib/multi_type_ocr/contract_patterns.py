"""
合約正則表達式模式庫

此模組包含用於提取合約關鍵欄位的正則表達式模式。
每個欄位提供多個模式變體以涵蓋不同的合約格式。

使用範例:
    import re
    from app.lib.multi_type_ocr.contract_patterns import PATTERNS

    text = "合約編號：ABC-2026-001"
    pattern = PATTERNS["contract_number"][0]
    match = re.search(pattern, text)
    if match:
        contract_number = match.group(1)
"""

# 正則表達式模式庫字典
PATTERNS = {
    # 合約編號模式
    "contract_number": [
        # 中文格式：合約編號、合約字號、合約號碼
        r"合約(?:編號|字號|號碼)?[：:]\s*([A-Z0-9\-]+)",
        # 英文格式：Contract No., Contract Number (支援 "No." 和 "No:")
        r"Contract\s+(?:No\.|Number)[：:]\s*([A-Z0-9\-]+)",
        # 簡化格式：編號
        r"(?:^|[^合約])編號[：:]\s*([A-Z0-9\-]+)",
    ],

    # 簽訂日期模式
    "signing_date": [
        # 西元年格式：2026年3月26日
        r"(?:簽訂日期|立約日期|契約日期)[：:]\s*(\d{4}年\d{1,2}月\d{1,2}日)",
        # 民國年格式（有空格）：中華民國 115 年 3 月 26 日
        r"中華民國\s+(\d{2,3})\s+年\s+(\d{1,2})\s+月\s+(\d{1,2})\s+日",
        # 民國年格式（無空格）：中華民國115年3月26日
        r"中華民國(\d{2,3})年(\d{1,2})月(\d{1,2})日",
        # 英文格式：Date: 2026-03-26
        r"(?:Date|Signing Date)[：:]\s*(\d{4}[-/]\d{1,2}[-/]\d{1,2})",
    ],

    # 生效日期模式
    "effective_date": [
        # 西元年格式
        r"(?:生效日期|起始日期)[：:]\s*(\d{4}年\d{1,2}月\d{1,2}日)",
        # 民國年格式（有空格）
        r"生效日期[：:]\s*中華民國\s+(\d{2,3})\s+年\s+(\d{1,2})\s+月\s+(\d{1,2})\s+日",
        # 相對日期：簽訂後生效、次日生效
        r"(?:簽訂後|合約簽署後)\s*([即立次]日|立即)\s*生效",
    ],

    # 甲方（第一方）模式
    "party_a": [
        # 標準格式：甲方：公司名稱
        r"甲方[：:]\s*([^\n\r（(]+)",
        # 立合約書人格式
        r"立合約書人[：:]?\s*([^\n\r（(]+)",
        # 第一方格式
        r"(?:第一方|賣方|委託方)[：:]\s*([^\n\r（(]+)",
    ],

    # 乙方（第二方）模式
    "party_b": [
        # 標準格式：乙方：公司名稱
        r"乙方[：:]\s*([^\n\r（(]+)",
        # 第二方格式
        r"(?:第二方|買方|受託方)[：:]\s*([^\n\r（(]+)",
        # 相對方格式
        r"相對方[：:]\s*([^\n\r（(]+)",
    ],

    # 甲方地址模式
    "party_a_address": [
        # 甲方地址格式
        r"甲方(?:地址|住址|營業地址)?[：:]\s*([^\n\r]+(?:市|縣|區|路|街|巷|弄|號)[^\n\r]*)",
        # 第一方地址格式
        r"(?:第一方|賣方)地址[：:]\s*([^\n\r]+)",
        # 地址在甲方後的格式
        r"地址[：:]\s*([^\n\r]+(?:市|縣|區|路|街|巷|弄|號)[^\n\r]*)",
    ],

    # 乙方地址模式
    "party_b_address": [
        # 乙方地址格式
        r"乙方(?:地址|住址|營業地址)?[：:]\s*([^\n\r]+(?:市|縣|區|路|街|巷|弄|號)[^\n\r]*)",
        # 第二方地址格式
        r"(?:第二方|買方)地址[：:]\s*([^\n\r]+)",
        # 簡化格式
        r"(?:^|\n)地址[：:]\s*([^\n\r]+)",
    ],

    # 合約金額模式
    "contract_amount": [
        # 含幣別格式：新台幣 1,000,000 元
        r"(?:合約金額|總價|總金額|契約金額)[：:]\s*(?:新台幣|新臺幣|NTD|TWD)?\s*([\d,]+)\s*元",
        # 簡單格式：500,000元整
        r"(?:總價|金額)[：:]\s*([\d,]+)\s*元整",
        # 英文格式：Total Amount: TWD 2,500,000
        r"Total\s+Amount[：:]\s*(?:TWD|NTD)?\s*([\d,]+)",
        # 大寫格式：壹佰萬元整（後續可擴展轉換邏輯）
        r"(?:合約金額|總金額)[：:]\s*(?:新台幣|新臺幣)?\s*([壹貳參肆伍陸柒捌玖拾佰仟萬億]+)元整",
    ],

    # 幣別模式
    "currency": [
        # 幣別標記
        r"幣別[：:]\s*(新台幣|新臺幣|美元|USD|TWD|NTD)",
        # 從金額中提取幣別
        r"(新台幣|新臺幣|NTD|TWD|USD)\s*[\d,]+",
        # 英文格式
        r"Currency[：:]\s*(TWD|NTD|USD|EUR|JPY)",
    ],

    # 付款方式模式
    "payment_method": [
        # 標準格式
        r"付款方式[：:]\s*([^\n\r]+)",
        # 英文格式
        r"Payment\s+Method[：:]\s*([^\n\r]+)",
        # 簡化格式：以...方式付款
        r"以\s*([^\n\r]+)\s*方式付款",
    ],

    # 付款期限模式
    "payment_deadline": [
        # 日期格式
        r"付款期限[：:]\s*(\d{4}年\d{1,2}月\d{1,2}日(?:前|前)?)",
        # 相對期限：簽約後30天
        r"(?:付款期限|給付期限)[：:]\s*(?:簽約|合約簽訂)後\s*(\d+)\s*(?:天|日|個工作天)",
        # 英文格式
        r"Payment\s+Deadline[：:]\s*([^\n\r]+)",
    ],

    # ========== 租賃合約專屬欄位 ==========

    # 租期起始日
    "date_start": [
        # 租期格式：租期：2026年3月27日 至 2027年3月26日
        r"租期[：:]\s*(\d{4}年\d{1,2}月\d{1,2}日)\s*(?:起|至)",
        # 起租日格式
        r"(?:起租日|租賃起始日|租期起算日)[：:]\s*(\d{4}年\d{1,2}月\d{1,2}日)",
        # 民國年格式
        r"(?:租期|起租日)[：:]\s*中華民國\s*(\d{2,3})\s*年\s*(\d{1,2})\s*月\s*(\d{1,2})\s*日",
        # 自...起格式
        r"自\s*(\d{4}年\d{1,2}月\d{1,2}日)\s*起(?:租|承租)",
    ],

    # 租期結束日
    "date_end": [
        # 租期至...止
        r"租期[：:]\s*\d{4}年\d{1,2}月\d{1,2}日\s*(?:至|迄|～)\s*(\d{4}年\d{1,2}月\d{1,2}日)",
        # 終止日格式
        r"(?:終止日|租期終止日|租賃終止日)[：:]\s*(\d{4}年\d{1,2}月\d{1,2}日)",
        # 民國年格式
        r"(?:至|迄)\s*中華民國\s*(\d{2,3})\s*年\s*(\d{1,2})\s*月\s*(\d{1,2})\s*日(?:止|為止)",
        # 簡化格式
        r"(?:至|迄|～)\s*(\d{4}年\d{1,2}月\d{1,2}日)\s*(?:止|為止)",
    ],

    # 月租金
    "monthly_rent": [
        # 每月租金格式
        r"(?:每月租金|月租金|租金)[：:]\s*(?:新台幣|新臺幣|NTD|TWD)?\s*([\d,]+)\s*元",
        # 租金每月格式
        r"租金\s*(?:每月|月付)\s*(?:新台幣|新臺幣)?\s*([\d,]+)\s*元",
        # 大寫格式
        r"(?:每月租金|月租金)[：:]\s*(?:新台幣|新臺幣)?\s*([壹貳參肆伍陸柒捌玖拾佰仟萬億]+)元整",
        # 英文格式
        r"(?:Monthly\s+)?Rent[：:]\s*(?:TWD|NTD)?\s*([\d,]+)",
    ],

    # 押金
    "deposit": [
        # 押金格式
        r"(?:押金|保證金|履約保證金)[：:]\s*(?:新台幣|新臺幣|NTD|TWD)?\s*([\d,]+)\s*元",
        # 相對押金：相當於...個月租金
        r"押金[：:]\s*(?:相當於|等同|為)\s*(\d+)\s*個月租金",
        # 大寫格式
        r"(?:押金|保證金)[：:]\s*(?:新台幣|新臺幣)?\s*([壹貳參肆伍陸柒捌玖拾佰仟萬億]+)元整",
        # 英文格式
        r"(?:Deposit|Security\s+Deposit)[：:]\s*(?:TWD|NTD)?\s*([\d,]+)",
    ],

    # 租賃標的地址
    "rental_address": [
        # 租賃標的格式
        r"(?:租賃標的|租賃房屋|房屋坐落|標的物)[：:]\s*([^\n\r]+(?:市|縣|區|路|街|巷|弄|號|樓)[^\n\r]*)",
        # 坐落格式
        r"坐落(?:於|在|：|:)\s*([^\n\r]+(?:市|縣|區|路|街|巷|弄|號|樓)[^\n\r]*)",
        # 房屋地址格式
        r"(?:房屋地址|物件地址|標的地址)[：:]\s*([^\n\r]+(?:市|縣|區|路|街|巷|弄|號|樓)[^\n\r]*)",
    ],

    # 承租人姓名
    "tenant_name": [
        # 承租人格式（排除括號內容如身分證字號）
        r"(?:承租人|房客|租賃人)[：:]\s*([^\n\r（(]+)",
        # 乙方即承租人
        r"乙方[（(]承租人[）)][：:]\s*([^\n\r（(]+)",
        # 簡化格式
        r"租客[：:]\s*([^\n\r（(]+)",
    ],

    # 承租人身分證字號
    "tenant_id_number": [
        # 身分證字號格式（台灣）
        r"(?:承租人|乙方).*?身(?:分|份)證(?:字號|號碼)[：:]\s*([A-Z][0-9]{9})",
        # 直接格式
        r"身(?:分|份)證(?:字號|號碼)[：:]\s*([A-Z][0-9]{9})",
        # 統一編號格式（公司承租）
        r"(?:承租人|乙方).*?統一編號[：:]\s*([0-9]{8})",
    ],

    # 承租人電話
    "tenant_phone": [
        # 電話格式（含手機、市話）
        r"(?:承租人|乙方).*?(?:電話|聯絡電話|手機)[：:]\s*([\d\-\(\)]{8,15})",
        # 行動電話格式
        r"(?:承租人|乙方).*?(?:行動電話|手機號碼)[：:]\s*(09\d{2}[-\s]?\d{3}[-\s]?\d{3})",
        # 簡化格式
        r"(?:電話|Tel|Phone)[：:]\s*([\d\-\(\)]{8,15})",
    ],

    # 出租人姓名
    "landlord_name": [
        # 出租人格式
        r"(?:出租人|房東|租賃人)[：:]\s*([^\n\r（(]+)",
        # 甲方即出租人
        r"甲方[（(]出租人[）)][：:]\s*([^\n\r（(]+)",
    ],

    # 出租人身分證字號
    "landlord_id_number": [
        # 身分證字號格式
        r"(?:出租人|甲方).*?身(?:分|份)證(?:字號|號碼)[：:]\s*([A-Z][0-9]{9})",
        # 統一編號格式
        r"(?:出租人|甲方).*?統一編號[：:]\s*([0-9]{8})",
    ],

    # 出租人電話
    "landlord_phone": [
        # 電話格式
        r"(?:出租人|甲方).*?(?:電話|聯絡電話|手機)[：:]\s*([\d\-\(\)]{8,15})",
        # 行動電話格式
        r"(?:出租人|甲方).*?(?:行動電話|手機號碼)[：:]\s*(09\d{2}[-\s]?\d{3}[-\s]?\d{3})",
    ],

    # 管理費
    "management_fee": [
        # 管理費格式
        r"(?:管理費|社區管理費)[：:]\s*(?:新台幣|新臺幣|NTD|TWD)?\s*([\d,]+)\s*元",
        # 每月管理費
        r"(?:每月)?管理費\s*(?:每月|月付)\s*(?:新台幣|新臺幣)?\s*([\d,]+)\s*元",
    ],

    # 停車費
    "parking_fee": [
        # 停車費格式
        r"(?:停車費|車位租金|停車位費用)[：:]\s*(?:新台幣|新臺幣|NTD|TWD)?\s*([\d,]+)\s*元",
        # 每月停車費
        r"(?:每月)?停車費\s*(?:每月|月付)\s*(?:新台幣|新臺幣)?\s*([\d,]+)\s*元",
    ],

    # 租金繳納日
    "payment_day": [
        # 每月第N日
        r"(?:租金繳納日|繳租日|付款日)[：:]\s*(?:每月)?(?:第)?\s*(\d{1,2})\s*日",
        # 每月月初/月底
        r"(?:租金繳納|繳租)[：:]\s*每月\s*(月初|月底|初|底)",
        # 於每月...日前繳納
        r"於每月\s*(\d{1,2})\s*日(?:前|前)?(?:繳納|支付)",
    ],
}


